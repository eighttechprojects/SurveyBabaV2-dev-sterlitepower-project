package com.surveybaba.PolygonInsideOutSide;

public class Point {

    double x; // lat utm e
    double y; // lon utm n

    public Point(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }
}
